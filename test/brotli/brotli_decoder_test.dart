  group('BitReader Tests', () {
    test('read_bits basic', () {
      // 0x0F = 0b00001111, 0xF0 = 0b11110000, 0xAB = 0b10101011
      final data = Uint8List.fromList([0x0F, 0xF0, 0xAB]);
      final reader = BrotliBitReader(data);

      // Ler 4 bits de 0x0F (little-endian) -> bits 0-3 = 0x0F
      expect(reader.read_bits(4), equals(0x0F));
      // Ler 4 bits mais -> bits 4-7 de 0x0F (high nibble) = 0x00
      expect(reader.read_bits(4), equals(0x00));
      // Ler 8 bits mais -> byte seguinte = 0xF0
      expect(reader.read_bits(8), equals(0xF0));
    });

    test('read_bits peek without advance', () {
      final data = Uint8List.fromList([0x0F, 0xF0]);
      final reader = BrotliBitReader(data);

      // Peek sem avançar
      expect(reader.read_bits(4, bits_to_skip: 0), equals(0x0F));
      expect(reader.read_bits(4, bits_to_skip: 0), equals(0x0F));

      // Agora avançar
      reader.read_bits(0, bits_to_skip: 4);
      expect(reader.read_bits(4), equals(0x00));
    });

    test('read_bits cross byte boundary', () {
      final data = Uint8List.fromList([0xFF, 0x00, 0xFF]);
      final reader = BrotliBitReader(data);

      expect(reader.read_bits(12), equals(0x0FF));
    });

    test('copy_bytes aligns to byte boundary', () {
      final data = Uint8List.fromList([0xFF, 0xAB, 0xCD, 0xEF]);
      final reader = BrotliBitReader(data);

      // Ler alguns bits para desalinhar
      reader.read_bits(3);

      // Copiar bytes deve alinhar primeiro
      final dest = List<int>.filled(2, 0);
      reader.copy_bytes(dest, 0, 2);

      expect(dest[0], equals(0xAB));
      expect(dest[1], equals(0xCD));
    });
  });

  group('MetaBlock Header Tests', () {
    test('decodeWindowBits - 16 bits default', () {
      final data = Uint8List.fromList([0x00]);
      final reader = BrotliBitReader(data);
      expect(decodeWindowBits(reader), equals(16));
    });

    test('decodeMetaBlockLength - empty last block', () {
      final data = Uint8List.fromList([0x03]); // ISLAST=1, ISLASTEMPTY=1
      final reader = BrotliBitReader(data);
      final meta = decodeMetaBlockLength(reader);
      expect(meta.inputEnd, isTrue);
      expect(meta.metaBlockLength, equals(0));
    });

    test('decodeMetaBlockLength - uncompressed block', () {
      // Cria um header para um bloco não comprimido
      final data = Uint8List.fromList([0x00, 0x00, 0x01]); // ISLAST=0
      final reader = BrotliBitReader(data);
      reader.read_bits(0); // Skip to align
      final meta = decodeMetaBlockLength(reader);
      expect(meta.inputEnd, isFalse);
    });
  });

