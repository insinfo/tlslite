            "-----END DH PARAMETERS-----\n";

      final (g, p) = parse(Uint8List.fromList(data.codeUnits));

      expect(p, BigInt.parse("88F896E39609BAF1CAB3C9380867E2D1BA8CEF05D406"
            "A2881066D5E00411D4132AE85D575B810500CBBDEDCEFD9AD1F71F882ECF4F21"
