import 'dart:typed_data';
import 'package:test/test.dart';
import 'package:tlslite/src/dh.dart';

void main() {
  group('DH Parse', () {
    test('test_pem', () {
      final data = 
            "-----BEGIN DH PARAMETERS-----\n"
            "MIGHAoGBAIj4luOWCbrxyrPJOAhn4tG6jO8F1AaiiBBm1eAEEdQTKuhdV1uBBQDL\n"
